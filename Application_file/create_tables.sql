
  CREATE TABLE "TRVL_USERS" 
   (	"USER_ID" NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"FULL_NAME" VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	"EMAIL" VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	"PASSWORD" VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	"CREATED_DATE" DATE DEFAULT SYSDATE, 
	 PRIMARY KEY ("USER_ID"));
  

  CREATE TABLE "TRVL_SERVICES" 
   (	"SERVICE_ID" NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"SERVICE_TYPE" VARCHAR2(20 BYTE), 
	"SERVICE_NAME" VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	"SOURCE_CITY" VARCHAR2(50 BYTE), 
	"DESTINATION_CITY" VARCHAR2(50 BYTE), 
	"PRICE" NUMBER(10,2) NOT NULL ENABLE, 
	 CHECK (service_type IN ('HOTEL','FLIGHT','TRAIN')) ENABLE, 
	 PRIMARY KEY ("SERVICE_ID"));

  CREATE TABLE "TRVL_BOOKINGS" 
